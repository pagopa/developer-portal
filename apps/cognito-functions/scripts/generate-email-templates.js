const fs = require('fs');
const path = require('path');

require('ts-node/register/transpile-only');

const mjml2html = require('mjml');
const { minify } = require('html-minifier-next');
const {
  EMAIL_TEMPLATE_BUILDERS,
} = require('../src/templates/template-sources');
const { SUPPORTED_LOCALES } = require('../src/i18n/locales');

const defaultMinificationOptions = {
  caseSensitive: true,
  collapseWhitespace: true,
  minifyCSS: true,
  removeEmptyAttributes: true,
};

const outputFilePath = path.join(
  __dirname,
  '..',
  'src',
  'templates',
  'generated',
  'precompiled-email-templates.ts'
);

const compileTemplate = async (buildTemplate, locale) => {
  const { errors, html } = mjml2html(buildTemplate(locale), {
    validationLevel: 'soft',
  });

  if (html.length === 0) {
    throw new Error(
      `Unable to compile ${buildTemplate.name || 'email template'} for locale ${locale}`
    );
  }

  if (errors.length > 0) {
    console.warn(
      `MJML validation warnings for ${buildTemplate.name || 'email template'} (${locale}): ${errors
        .map(({ message }) => message)
        .join(', ')}`
    );
  }

  return minify(html, defaultMinificationOptions);
};

const buildTemplates = async () => {
  const precompiledTemplates = {};

  for (const [templateId, buildTemplate] of Object.entries(
    EMAIL_TEMPLATE_BUILDERS
  )) {
    precompiledTemplates[templateId] = {};

    for (const locale of SUPPORTED_LOCALES) {
      precompiledTemplates[templateId][locale] = await compileTemplate(
        buildTemplate,
        locale
      );
    }
  }

  return precompiledTemplates;
};

const writeTemplatesFile = async () => {
  const precompiledTemplates = await buildTemplates();
  const outputContents = `/* This file is generated by scripts/generate-email-templates.js. */
export const PRECOMPILED_EMAIL_TEMPLATES = ${JSON.stringify(
    precompiledTemplates,
    null,
    2
  )} as const;
`;

  fs.mkdirSync(path.dirname(outputFilePath), { recursive: true });
  fs.writeFileSync(outputFilePath, outputContents);
};

writeTemplatesFile().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});